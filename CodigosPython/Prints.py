#Print es un comando bastante simple realmente, aunque cuenta con varias opciones interesantes.
print("Hola Mundo") #Este es un print simple que ya todo deben conocer, es facil de entender y bastante utilizado.
print(f"Hola") #Agregar una f antes de las comillas permite poder utilizar variables de forma mas simple y rapida.
palabra = "Mundo"
print(f"Hola {palabra}") #Como pueden ver aqui, pude utilizar la variable "palabra" sin la neceidad de salir de las comillas, aunque utilizar la f tambien sirve para hacer calculos entre variables de forma mas rapida.
edad = 21
año = 2025
print(f"Hola, tengo {edad} y naci el {año - edad}") #Como ven en el codigo, pude calcular el año de nacimiento de forma rapida solo con 2 variables dentro del print y sin necesidad de una tercera variable.
